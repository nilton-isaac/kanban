const LEGACY_THEME_ALIAS = {
  detective: 'darkest',
  darkwouls: 'darksouls',
  darksouls_theme: 'darksouls',
  'synth-dark': 'dark',
  'synth-light': 'light',
}

const VALID_THEME_IDS = new Set(Object.keys(THEMES))

export function normalizeThemeId(themeId) {
  const raw = String(themeId || '').trim().toLowerCase()
  const normalized = LEGACY_THEME_ALIAS[raw] || raw
  return VALID_THEME_IDS.has(normalized) ? normalized : DEFAULT_THEME_ID
}

export function getThemeDefinition(themeId) {
  return THEMES[normalizeThemeId(themeId)] || THEMES[DEFAULT_THEME_ID]
}

export function getThemeTone(themeId) {
  return getThemeDefinition(themeId).tone
}

export function getStandupThemeId(themeId) {
  return getThemeTone(themeId) === 'light' ? 'light' : 'dark'
}

export function isAudioReactiveTheme(themeId) {
  return AUDIO_REACTIVE_THEME_IDS.includes(normalizeThemeId(themeId))
}

export function applyTheme(themeId) {
  const theme = getThemeDefinition(themeId)
  const root = document.documentElement

  Object.entries(theme.vars).forEach(([key, value]) => {
    root.style.setProperty(key, value)
  })

  root.style.colorScheme = theme.tone === 'light' ? 'light' : 'dark'
  root.setAttribute('data-theme', theme.id)
  document.body.setAttribute('data-theme', theme.id)
}
